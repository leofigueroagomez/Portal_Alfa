create index if not exists project_purchase_lines_quote_item_id_idx
  on public.project_purchase_lines(quote_item_id);

create index if not exists project_purchase_lines_supplier_idx
  on public.project_purchase_lines(supplier);

create index if not exists project_purchase_lines_status_idx
  on public.project_purchase_lines(purchase_status);

create index if not exists project_purchase_events_line_id_idx
