create or replace function public.set_project_deliveries_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists set_project_deliveries_updated_at
  on public.project_deliveries;

create trigger set_project_deliveries_updated_at
before update on public.project_deliveries
for each row
execute function public.set_project_deliveries_updated_at();

